create table #用户临时表(ID int, IDValues int)
--系统临时表:
create table ##系统临时表(ID int, IDValues int)
/*
（临时表可以在“数据库”→“系统数据库”→“tempdb”→“临时表”中找到）
本地/用户临时表的名称以#打头；它们仅对当前的用户连接是可见的；
全局/系统临时表的名称以##打头，创建后对任何用户都是可见的
（临时意味着在关掉当前查询文件或执行了其他查询后表会自动被删除）
（临时表作用：当查询涉及多个大表时，做笛卡尔积可能会导致查询超时，可以先对大表做一定查询后放在临时表内再做笛卡尔积会方便很多）
（临时表弊端：占用空间，一定程度上影响数据库执行时候的性能）
*/